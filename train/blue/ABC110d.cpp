const ll MAX = 1000000; //テーブルの数
const ll MOD = 1e9+7; //mod宣言

long long fac[MAX], finv[MAX], inv[MAX];
// finv ->階乗割り算
// fac ->階乗掛け算

// テーブルを作る前処理
void COMinit() {
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;
    for (int i = 2; i < MAX; i++){
        fac[i] = fac[i - 1] * i % MOD;
        inv[i] = MOD - inv[MOD%i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}

// 二項係数計算
long long COM(int n, int k){
    if (n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return (fac[n] * (finv[k] * finv[n - k] % MOD)) % MOD;
}

int main(){
    COMinit();
    ll n, m; cin >> n >> m;
    int count = 0;
    ll ans = 1;
    for(ll i = 2; (i * i) <= m + 10; i++){
        int f = 0;
        while(m % i == 0){
            m /= i;
            count++;
            f = 1;
        }
        if(f){
            ans *= COM(n+count-1, count);
            ans %= MOD;
            count = 0;  
        }
    }
    if(m != 1){
        ans *= COM(n, 1);
        ans %= MOD;
    }
    cout << ans << endl;
}